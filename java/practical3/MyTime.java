
public class MyTime {
    int day, month, year, hour, min, sec;
    String ampm;
    public MyTime() {
    }
    public MyTime(int day, int month, int year, int hour, int min, int
sec, String ampm) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.hour = hour;
        this.min = min;
        this.sec = sec;
        this.ampm = ampm;
}
}